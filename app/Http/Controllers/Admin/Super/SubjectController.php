     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $teachers = (new User())
            ->getAllTeachers()
            ->get(['id','name','surname','patronymic']);

        $teachers = (new User())->groupFullName($teachers);

        return view('admin.super.subject.create',compact('teachers'));
    }

